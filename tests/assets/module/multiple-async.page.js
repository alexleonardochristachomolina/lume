export default async function* () {
  const pages = [1, 2];

  for (const num of pages) {
    yield new Promise((res) =>
      setTimeout(() =>
        res({
          title: `Multiple page ${num}`,
          url: (page) => `/async/${page.data.num}/`,
          content: `Async Page ${num}`,
          num,
        }), 100)
    );
  }
}
